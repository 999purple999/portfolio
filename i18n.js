const LANGS = ['en', 'it', 'zh'];
const LABELS = { en: 'EN', it: 'IT', zh: '中' };

function detect() {
  try {
    const saved = localStorage.getItem('portfolio:lang');
    if (saved && LANGS.includes(saved)) return saved;
  } catch {}
  // Default: English (richiesta utente). Solo auto-detect se IT/ZH evidente.
  const nav = (navigator.language || 'en').toLowerCase();
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('it')) return 'it';
  return 'en';
}

let current = detect();

function get(key) {
  return TR[current]?.[key] ?? TR.en[key] ?? key;
}

function set(lang) {
  if (!LANGS.includes(lang)) return;
  current = lang;
  try { localStorage.setItem('portfolio:lang', lang); } catch {}
  apply();
}

function apply() {
  document.documentElement.lang = current;
  document.title = get('meta.title');
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const v = get(key);
    if (v != null) {
      // Conserva eventuali figli interattivi (a, button, svg)
      if (el.children.length === 0) el.textContent = v;
      else {
        // Sostituisci solo i text-node diretti (lascia gli element children).
        // Per i casi complessi usiamo [data-i18n-html] per innerHTML.
        el.childNodes.forEach((n) => { if (n.nodeType === 3) n.textContent = ''; });
        el.insertBefore(document.createTextNode(v), el.firstChild);
      }
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    const v = get(key);
    if (v != null) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
    const [attr, key] = el.getAttribute('data-i18n-attr').split(':');
    const v = get(key);
    if (v != null) el.setAttribute(attr, v);
  });
  // Update switcher state
  document.querySelectorAll('[data-lang]').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === current);
  });
}

window.__i18n = { get, set, apply, langs: LANGS, labels: LABELS, current: () => current };

document.addEventListener('DOMContentLoaded', () => {
  // Bind switcher buttons
  document.querySelectorAll('[data-lang]').forEach((btn) => {
    btn.addEventListener('click', () => set(btn.dataset.lang));
  });
  apply();
});
